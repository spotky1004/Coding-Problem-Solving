let numbers = [];
for (let n = 0; n <= 1000; n++) {
    let numDigits = (n+"").split("");
    let str = "+[";
    for (let i = 0; i < numDigits.length-1; i++) {
        if (numDigits[i] === "7") {
            numDigits[i] = (+numDigits[i+1]+10).toString(36);
            numDigits.splice(i+1, 1);
            break;
        }
    }
    for (let i = 0, l = numDigits.length; i < l; i++) {
        str += "+[" + digit[numDigits[i]] + "]";
    }
    str += "]";
    str = str.replace(/\+\+/g, "");
    str = str.replace(/\+\[\]/, "]");
    numbers.push(str);
}

