/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[T], ...N] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
const outs = [];
for (let i = 1; i <= T; i++) {
  let out = "";
  const [n] = N[i - 1];
  if (n <= 25) out = "World Finals";
  else if (n <= 1000) out = "Round 3";
  else if (n <= 4500) out = "Round 2";
  else out = "Round 1";

  outs.push(`Case #${i}: ${out}`);
}

// output
return outs.join("\n");
}
