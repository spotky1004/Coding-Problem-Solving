/**
 * @param {string} input 
 */
function solve(input) {
// input
const [N, S] = input
  .trim()
  .split("\n");

// code
const acc = Array(26).fill(0);
for (const s of S) {
  const idx = parseInt(s, 36) - 10;
  if (isNaN(idx) || idx < 0) continue;

  acc[idx]++;
}

// output
return Math.max(...acc);
}
