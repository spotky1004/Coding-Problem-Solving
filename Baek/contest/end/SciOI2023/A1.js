/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[T], ...cases] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
const out = [];
for (const [N, M, K] of cases) {
  let loop = 0;
  let pplLeft = N;
  while (pplLeft > 0) {
    loop++;
    if (loop % 2 === 1) {
      pplLeft -= K * M;
    } else {
      pplLeft += 1;
    }
    if (loop > 100) {
      loop = -1;
      break;
    }
  }
  out.push(loop);
}

// output
return out.join("\n");
}
