/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], ...lines] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));
const works = lines.splice(0, N);
const [M] = lines.shift();
const laterWorks = lines;

// code
class Heap {
  tree = [null];
  compFunc = (a, b) => a - b;

  constructor(compFunc) {
    if (compFunc) this.compFunc = compFunc;
  }

  get size() {
    return this.tree.length - 1;
  }

  first() {
    return this.tree[1];
  }

  push(v) {
    const tree = this.tree;
    const compFunc = this.compFunc;

    let curIdx = tree.length;
    tree.push(v);
    while (curIdx !== 1) {
      const parentIdx = Math.floor(curIdx / 2);
      if (compFunc(v, tree[parentIdx]) >= 0) break;
      this.swap(curIdx, parentIdx);
      curIdx = parentIdx;
    }
  }

  pop() {
    const tree = this.tree;
    const compFunc = this.compFunc;
    if (tree.length === 1) return null;
    
    const popedNode = tree[1];
    if (tree.length === 2) {
      this.tree = [null];
      return popedNode;
    }
    
    const lastNode = tree.pop();
    tree[1] = lastNode;
    let curIdx = 1;
    while (true) {
      const childIdx = tree.length <= curIdx * 2 + 1 || compFunc(tree[curIdx * 2], tree[curIdx * 2 + 1]) <= 0 ? curIdx * 2 : curIdx * 2 + 1;
      if (
        tree.length <= childIdx ||
        compFunc(lastNode, tree[childIdx]) <= 0
      ) break;
      this.swap(curIdx, childIdx);
      curIdx = childIdx;
    }
    
    return popedNode;
  }

  swap(a, b) {
    const tmp = this.tree[b];
    this.tree[b] = this.tree[a];
    this.tree[a] = tmp;
  }
}



let t = 0;
let laterIdx = 0;
function updateLaterWorks() {
  while (laterIdx !== M && laterWorks[laterIdx][0] <= t) {
    heap.push([laterWorks[laterIdx][1], laterWorks[laterIdx][2]]);
    laterIdx++;
  }
}

const heap = new Heap((a, b) => (a[1] - b[1]) || (a[0] - b[0]));
for (const work of works) heap.push(work);
while (heap.size > 0) {
  updateLaterWorks();
  const work = heap.pop();
  const laterAddLeft = laterIdx === M ? Infinity : laterWorks[laterIdx][0] - t;
  const dt = Math.min(work[0], laterAddLeft);
  t += dt;
  work[0] -= dt;
  if (t > work[1]) return "NO";
  updateLaterWorks();
  if (work[0] !== 0) heap.push(work);
  if (heap.size === 0 && laterIdx !== M) {
    t = laterWorks[laterIdx][0];
    updateLaterWorks();
  }
}

// output
return `YES\n${t}`;
}
