/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N, M], A, ...cond] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
A.sort((a, b) => a - b);
let quality = 0;
for (const [t, q] of cond) {
  while (quality < q) {
    if (A.length === 0) return -1;
    quality += A.pop() + t;
  }
}
quality += A.reduce((a, b) => a + b + cond[cond.length - 1][0], 0);

// output
return quality + "";
}
