/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], Xs] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(BigInt));

// code
function bigSqrt(x, m) {
  let left = 0n, right = x * m;
  while (left < right) {
    const mid = (left + right) / 2n;
    const midPow = mid ** 2n;
    if (midPow > x) right = mid - 1n;
    else if (midPow < x) left = mid + 1n;
    else return mid;
  }
  return left;
}

const out = [];
for (const X of Xs) {
  out.push(bigSqrt(X, 1n)**2n === X ? 1 : 0);
}

// output
return out.join(" ");
}
