/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N, K]] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
const dp = Array(2e5 + 1).fill(null);
dp[N] = [0, 1];
const queue = [N];
for (const pos of queue) {
  const [time, count] = dp[pos];
  for (const next of [pos - 1, pos + 1, 2 * pos]) {
    if (next >= dp.length || next < 0) continue;
    if (dp[next] === null) {
      dp[next] = [time + 1, count];
      queue.push(next);
    } else if (dp[next][0] === time + 1) {
      dp[next][1] += count;
    }
  }
}

// output
return `${dp[K][0]}\n${dp[K][1]}`;
}
