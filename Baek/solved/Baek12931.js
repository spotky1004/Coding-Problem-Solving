/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], B] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
let count = 0;
while (B.some(Bi => Bi > 0)) {
  if (B.every(Bi => Bi % 2 === 0)) {
    for (let i = 0; i < N; i++) {
      B[i] /= 2;
    }
    count++;
  } else {
    for (let i = 0; i < N; i++) {
      if (B[i] % 2 !== 0) {
        B[i]--;
        count++;
      }
    }
  }
}

// output
return count;
}
