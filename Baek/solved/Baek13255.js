/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], [K], A] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
let p = 1;
for (const Ai of A) {
  p = p * (1 - Ai / N) + (1 - p) * Ai / N;
}
const out = p * N;

// output
return out;
}
