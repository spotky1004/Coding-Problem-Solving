/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], A] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(BigInt));

// code
let sum = 0n;
const MAX = 1_000_000_000n;
for (let b = 1n; b < MAX; b <<= 1n) {
  const counts = [0n, 0n];
  for (const Ai of A) {
    const isOne = Ai & b;
    if (isOne) {
      const tmp = counts[0];
      counts[0] = counts[1];
      counts[1] = tmp + 1n;
    } else {
      counts[0]++;
    }
    sum += b * counts[1];
  }
}

// output
return sum;
}
