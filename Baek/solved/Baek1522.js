/**
 * @param {string} input 
 */
function solve(input) {
// input
const S = input
  .trim();

// code
let minCount = Infinity;
for (let i = 0; i < S.length; i++) {
  const Sp = S.slice(i) + S.slice(0, i);
  let l = 0, r = S.length - 1;
  let count = 0;
  loop: while (l < r) {
    if (Sp[l] === "a") {
      l++;
      continue;
    }
    while (Sp[r] !== "a") {
      r--;
      if (l >= r) break loop;
    }
    count++;
    l++;
    r--;
  }
  minCount = Math.min(minCount, count);
}

// output
return minCount;
}
