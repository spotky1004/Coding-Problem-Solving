/**
 * @param {string} input 
 */
function solve(input) {
// input
const [N, ...namu] = input
  .trim()
  .split("\n")
  .map(Number);

// code
/**
 * @param {number} a 
 * @param {number} b 
*/
function gcd(a, b) {
  return b ? gcd(b, a%b) : a;
}



let namuGcd = namu[1] - namu[0];
for (let i = 2; i < N; i++) namuGcd = gcd(namuGcd, namu[i] - namu[i - 1]);

// output
return (namu[N - 1] - namu[0]) / namuGcd - N + 1;
}
