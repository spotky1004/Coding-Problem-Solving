/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N, M, K], ...scores] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
let sum = 0;

scores.sort((a, b) => a[1] - b[1]);
for (let i = 0; i < K; i++) {
  sum += scores.pop()[0];
}

scores.sort((a, b) => a[0] - b[0]);
for (let i = 0; i < M; i++) {
  sum += scores.pop()[0];
}

// output
return sum;
}
