/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N]] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
const p = 999017;
const m = Array(N).fill(0);
m[0] = 0;
m[1] = 1;
m[2] = 1;
for (let i = 3; i <= N; i++) {
  m[i] = (2 * m[i - 1]) % p;
}

const dp = Array(N + 1).fill(0);
for (let i = 1; i <= N; i++) {
  dp[i] += m[i];
  for (let j = 1; j < i; j++) {
    dp[i] += (m[j] * dp[i - j]) % p;
  }
  dp[i] %= p;
}

let fact = 1;
for (let i = 2; i <= N; i++) {
  fact = (fact * i) % p;
}

const out = (fact - dp[N] + p) % p;

// output
return out;
}
