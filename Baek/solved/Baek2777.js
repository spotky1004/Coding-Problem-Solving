/**
 * @param {string} input 
 */
function solve(input) {
// input
const [T, ...cases] = input
  .trim()
  .split("\n")
  .map(Number);

// code
const out = [];
for (const N of cases) {
  const factors = Array(10).fill(0);
  let n = N;
  for (let i = 9; i >= 2; i--) {
    while (n % i === 0) {
      factors[i]++;
      n /= i;
    }
  }

  if (n !== 1) {
    out.push(-1);
    continue;
  }
  out.push(Math.max(1, factors.reduce((a, b) => a + b, 0)));
}

// output
return out.join("\n");
}
