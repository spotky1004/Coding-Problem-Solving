/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], ...queries] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
const out = [];
const stack = [];
for (const [type, X] of queries) {
  if (type === 1) stack.push(X);
  else if (type === 2) out.push(stack.length > 0 ? stack.pop() : -1);
  else if (type === 3) out.push(stack.length);
  else if (type === 4) out.push(stack.length > 0 ? 0 : 1);
  else if (type === 5) out.push(stack.length > 0 ? stack[stack.length - 1] : -1);
}

// output
return out.join("\n");
}
