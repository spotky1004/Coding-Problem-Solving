/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[N], nums] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
const MAX_NUM = 20;
let dp = Array(MAX_NUM + 1).fill(0n);
dp[nums[0]] = 1n;
for (let i = 1; i < N - 1; i++) {
  const num = nums[i];
  const newDp = Array(MAX_NUM + 1).fill(0n);
  for (let j = 0; j <= MAX_NUM; j++) {
    newDp[j] = (dp[j + num] ?? 0n) + (dp[j - num] ?? 0n);
  }
  dp = newDp;
}

// output
return dp[nums[N - 1]] + "";
}
