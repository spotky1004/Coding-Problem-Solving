/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[T], ...cases] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(Number));

// code
function search(n, i = 0, curExpr = "", zeroExprs = []) {
  if (i === n) {
    if (eval(curExpr.replace(/ /g, "")) === 0) {
      zeroExprs.push(curExpr);
    }
    return;
  }

  curExpr += (i + 1);
  if (i < n - 1) {
    search(n, i + 1, curExpr + " ", zeroExprs);
    search(n, i + 1, curExpr + "+", zeroExprs);
    search(n, i + 1, curExpr + "-", zeroExprs);
  } else {
    search(n, i + 1, curExpr, zeroExprs);
  }

  return zeroExprs;
}

const out = [];
for (const [N] of cases) {
  const zeroExprs = search(N);
  out.push(zeroExprs.join("\n") + "\n");
}

// output
return out.join("\n");
}
