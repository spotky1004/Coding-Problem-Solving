/**
 * @param {string} input 
 */
function solve(input) {
// input
const [[T], ...cases] = input
  .trim()
  .split("\n")
  .map(line => line.split(" ").map(BigInt));

// code
/**
 * @param {bigint} a 
 * @param {bigint} b 
 * @param {bigint} p
*/
function powMod(a, b, p) {
  if (b === 0n) return 1n;
  let out = 1n;
  let curMul = a;
  let bin = 1n;
  while (bin <= b) {
    if (b & bin) {
      out = out*curMul % p;
    }
    bin *= 2n;
    curMul = curMul**2n % p;
  }
  return out;
}

/**
 * @param {number} n 
*/
function genPrimes(n) {
  /** @type {number[]} */
  const primes = [];
  const net = Array(n).fill(true);
  for (let i = 2; i <= n; i++) {
    if (net[i]) primes.push(i);
    for (const p of primes) {
      const a = i * p;
      if (a > n) break;
      net[a] = false;
      if (i % p === 0) break;
    }
  }
  return primes;
}

const primes = genPrimes(Math.ceil(Math.sqrt(1e9))).map(BigInt);
function calcEularPhi(n) {
  let out = n;
  for (const p of primes) {
    if (n < p * p) break;
    if (n % p !== 0n) continue;
    out -= out / p;
    while (n % p === 0n) n /= p;
  }
  if (n !== 1n) out -= out / n;
  return out;
}



const out = [];
for (const [N, M] of cases) {
  if (N === M) {
    out.push(0n);
    continue;
  }

  const mods = [M];
  while (mods[mods.length - 1] !== 1n) mods.push(calcEularPhi(mods[mods.length - 1]));
  
  let cur = 0n;
  while (mods.length > 0) cur = powMod(N, cur, mods.pop());
  out.push(cur);
}

// output
return out.join("\n");
}
